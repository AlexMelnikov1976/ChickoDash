// ──────────────────────────────────────────────────────────────────────────
// Tooltip engine
// ──────────────────────────────────────────────────────────────────────────

(function() {
  let tooltipEl = null;

  function escapeHtml(s) {
    if (s == null) return '';
    return String(s)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;');
  }

  function ensureTooltip() {
    if (tooltipEl) return tooltipEl;
    tooltipEl = document.createElement('div');
    tooltipEl.id = 'mkt-tooltip';
    tooltipEl.setAttribute('role', 'tooltip');
    document.body.appendChild(tooltipEl);
    return tooltipEl;
  }

  function show(el) {
    const key = el.getAttribute('data-mkt-tip');
    if (!key) return;

    const tip = MKT_TIPS[key];
    if (!tip) {
      console.warn('[mkt-tip] missing key:', key);
      return;
    }

    const t = ensureTooltip();
    t.innerHTML =
      '<div class="mkt-tip-title">' + escapeHtml(tip.title || key) + '</div>' +
      '<div class="mkt-tip-row"><b>Что:</b> ' + escapeHtml(tip.what) + '</div>' +
      '<div class="mkt-tip-row"><b>Как считается:</b> ' + escapeHtml(tip.how) + '</div>' +
      '<div class="mkt-tip-row"><b>Как читать:</b> ' + escapeHtml(tip.read) + '</div>';

    t.classList.add('mkt-tip-visible');
    position(t, el);
  }

  function hide() {
    if (tooltipEl) tooltipEl.classList.remove('mkt-tip-visible');
  }

  function position(t, el) {
    // Сначала отрисовать с временной позицией, чтобы получить реальный размер
    t.style.left = '0px';
    t.style.top = '0px';

    const r = el.getBoundingClientRect();
    const tw = t.offsetWidth;
    const th = t.offsetHeight;
    const vw = window.innerWidth;
    const vh = window.innerHeight;
    const margin = 8;

    // Default: под плашкой, по центру
    let top = r.bottom + margin;
    let left = r.left + (r.width - tw) / 2;

    // Не помещается снизу → наверх
    if (top + th > vh - margin) {
      top = r.top - th - margin;
    }
    // Если и сверху не помещается — прижать к верху viewport
    if (top < margin) top = margin;

    // Горизонтальная корректировка
    if (left + tw > vw - margin) left = vw - tw - margin;
    if (left < margin) left = margin;

    t.style.left = left + 'px';
    t.style.top = top + 'px';
  }

  // Event delegation на document — работает и для динамически отрисованных
  // элементов вкладки Маркетинг (mktDraw перерисовывает DOM при смене периода).
  document.addEventListener('mouseover', function(e) {
    const el = e.target.closest('[data-mkt-tip]');
    if (!el) return;
    show(el);
  });

  document.addEventListener('mouseout', function(e) {
    const el = e.target.closest('[data-mkt-tip]');
    if (!el) return;
    // Не скрывать при переходе курсора между дочерними элементами плашки
    if (e.relatedTarget && el.contains(e.relatedTarget)) return;
    hide();
  });

  // Скролл во время hover — закрыть, чтобы tooltip не «зависал» в воздухе
  window.addEventListener('scroll', hide, { passive: true, capture: true });

  // Таб скрыт / переключение вкладки → закрыть
  document.addEventListener('visibilitychange', function() {
    if (document.hidden) hide();
  });
})();
