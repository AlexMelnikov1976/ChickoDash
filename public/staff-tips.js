// ──────────────────────────────────────────────────────────────────────────
// Tooltip engine
// ──────────────────────────────────────────────────────────────────────────

(function() {
  let tooltipEl = null;

  function escapeHtml(s) {
    if (s == null) return '';
    return String(s)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;');
  }

  function ensureTooltip() {
    if (tooltipEl) return tooltipEl;
    tooltipEl = document.createElement('div');
    tooltipEl.id = 'staff-tooltip';
    tooltipEl.setAttribute('role', 'tooltip');
    document.body.appendChild(tooltipEl);
    return tooltipEl;
  }

  function show(el) {
    const key = el.getAttribute('data-staff-tip');
    if (!key) return;

    const tip = STAFF_TIPS[key];
    if (!tip) {
      console.warn('[staff-tip] missing key:', key);
      return;
    }

    const t = ensureTooltip();
    t.innerHTML =
      '<div class="staff-tip-title">' + escapeHtml(tip.title || key) + '</div>' +
      '<div class="staff-tip-row"><b>Что:</b> ' + escapeHtml(tip.what) + '</div>' +
      '<div class="staff-tip-row"><b>Как считается:</b> ' + escapeHtml(tip.how) + '</div>' +
      '<div class="staff-tip-row"><b>Как читать:</b> ' + escapeHtml(tip.read) + '</div>';

    t.classList.add('staff-tip-visible');
    position(t, el);
  }

  function hide() {
    if (tooltipEl) tooltipEl.classList.remove('staff-tip-visible');
  }

  function position(t, el) {
    t.style.left = '0px';
    t.style.top = '0px';

    const r = el.getBoundingClientRect();
    const tw = t.offsetWidth;
    const th = t.offsetHeight;
    const vw = window.innerWidth;
    const vh = window.innerHeight;
    const margin = 8;

    let top = r.bottom + margin;
    let left = r.left + (r.width - tw) / 2;

    if (top + th > vh - margin) {
      top = r.top - th - margin;
    }
    if (top < margin) top = margin;

    if (left + tw > vw - margin) left = vw - tw - margin;
    if (left < margin) left = margin;

    t.style.left = left + 'px';
    t.style.top = top + 'px';
  }

  document.addEventListener('mouseover', function(e) {
    const el = e.target.closest('[data-staff-tip]');
    if (!el) return;
    show(el);
  });

  document.addEventListener('mouseout', function(e) {
    const el = e.target.closest('[data-staff-tip]');
    if (!el) return;
    if (e.relatedTarget && el.contains(e.relatedTarget)) return;
    hide();
  });

  window.addEventListener('scroll', hide, { passive: true, capture: true });

  document.addEventListener('visibilitychange', function() {
    if (document.hidden) hide();
  });
})();
